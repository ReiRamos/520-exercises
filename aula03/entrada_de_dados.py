#Perguntar o nome de um aluno e qual curso irá realizar, gravar em uma variável e mostrar na tela o seu valor

nome = input("Digite o nome do aluno: ")
curso = input("Digite o nome do curso: ")

print(nome, curso)

#Concatenar variáveis e mostrar a seguinte mensagem "O aluno ___    faz o curso de __ __"

print("O aluno %s faz o curso de %s" %(nome, curso))

