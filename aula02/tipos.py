#Criar uma variável
var1 = None
print(type(var1))

# Coloque nela um tipo numérico
var1 = 10
print(type(var1))
print(var1)

# Coloque nela um tipo booleano
var1 = False
print(type(var1))
print(var1)

# Coloque nela um tipo String com aspas simples
var1 = 'Variável 1'
print(type(var1))
print(var1)

# Coloque nela um tipo String com aspas duplas
var1 = "Variável 2"
print(type(var1))
print(var1)

# Coloque uma lista dentro dela
var1 = []
print(type(var1))
print(var1)

# Coloque uma tupla dentro dela
var1 = ()
print(type(var1))
print(var1)

# Inclua um novo valor nessa tupla
var1 = (1,2)
print(type(var1))
print(var1)

# Coloque um dicionario dentro dela
var1 = {}
print(type(var1))
print(var1)
